/**
 * Enum que representa os níveis de severidade de uma alergia.
 */
export enum AllergySeverity {
  MILD = 'MILD',           // Reações leves, como coceira ou irritação local
  MODERATE = 'MODERATE',   // Reações moderadas, como urticária ou inchaço
  SEVERE = 'SEVERE',       // Reações graves, como dificuldade para respirar
  CRITICAL = 'CRITICAL'    // Reações potencialmente fatais, como anafilaxia
}